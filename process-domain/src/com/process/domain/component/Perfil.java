package com.process.domain.component;

public class Perfil {
	
	private String in_activo;
	private String fe_desde;
	private String fe_hasta;
	private String grp;
	private String prf;
	private String nb_prf;
	private String puesto;
	
	
	
	public String getPuesto() {
		return puesto;
	}
	public void setPuesto(String puesto) {
		this.puesto = puesto;
	}
	public String getNb_prf() {
		return nb_prf;
	}
	public void setNb_prf(String nb_prf) {
		this.nb_prf = nb_prf;
	}

	public String getFe_hasta() {
		return fe_hasta;
	}
	public void setFe_hasta(String fe_hasta) {
		this.fe_hasta = fe_hasta;
	}	
	public String getIn_activo() {
		return in_activo;
	}
	public void setIn_activo(String in_activo) {
		this.in_activo = in_activo;
	}
	public String getFe_desde() {
		return fe_desde;
	}
	public void setFe_desde(String fe_desde) {
		this.fe_desde = fe_desde;
	}
	public String getGrp() {
		return grp;
	}
	public void setGrp(String grp) {
		this.grp = grp;
	}
	public String getPrf() {
		return prf;
	}
	public void setPrf(String prf) {
		this.prf = prf;
	}
	
	
	
	
}
